class Solution200 {
    public int numIslands(char[][] grid) {
        int cn = 0;
        for (int i = 0; i < grid.length; i++) {
            for (int j = 0; j < grid[0].length; j++) {
                if (grid[i][j] == '1'){
                    infect(grid, i, j);
                    cn++;
                }
            }
        }
        return cn;
    }

    private void infect(char[][] g, int i, int j) {
        if (i < 0 || i >= g.length || j < 0 || j >= g[0].length || g[i][j] != '1'){
            return;
        }
        if (g[i][j] == '1'){
            g[i][j] = '2';
        }
        infect(g, i - 1, j);
        infect(g, i + 1, j);
        infect(g, i, j - 1);
        infect(g, i, j + 1);
    }
}
