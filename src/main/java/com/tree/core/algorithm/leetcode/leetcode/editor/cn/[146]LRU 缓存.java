class LRUCache146 {
    int size;
    Map<Integer, Node> map;
    Node head;
    Node tail;
    public LRUCache146(int capacity) {
        size = capacity;
        map = new HashMap<>();
        head = tail = new Node();
    }
    
    public int get(int key) {
        Node node = map.get(key);
        if (node == null){
            return -1;
        }
        moveToTail(node);
        return node.val;
    }

    private void moveToTail(Node node) {
        Node pre = node.pre;
        Node next = node.next;
        pre.next = next;
        next.pre = pre;
        tail.next = node;
        node.pre = tail;
        tail = node;
        tail.next = null;
    }

    public void put(int key, int value) {
        Node node = map.get(key);
        if (node != null){
            node.val = value;
            moveToTail(node);
            return;
        }
        if (map.size() == size){
            Node next = head.next;
            head.next = next.next;
            next.next.pre = head;
            next.pre = next.next = null;
            size--;
        }
        Node newNode = new Node();
        newNode.val =value;
        newNode.key =key;
        tail.next = newNode;
        newNode.pre = tail;
        tail = newNode;
        size++;

    }

    class Node{
        int key;
        int val;
        Node next;
        Node pre;
    }
}

